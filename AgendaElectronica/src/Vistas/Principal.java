    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        /*El procedimiento es similar que en Next, en este caso mientras indice_now > 0, le resta 1
        osea que cuando llegue el indice a 0 deja de contar.
        */
        if(indice_now > 0)
        {
            indice_now--;
            txtDni.setText(Integer.toString(vector_dni[indice_now]));
            txtApellido.setText(vector_apellido[indice_now]);
            txtNombre.setText(vector_nombre[indice_now]);
            txtTelefono.setText(Integer.toString(vector_telefono[indice_now]));
            txtDomicilio.setText(vector_domicilio[indice_now]);
            txtNacimiento.setText(vector_nacimiento[indice_now]);
        }
        //Aca visualizo el numero de indice por donde voy
        txtIndice.setText(Integer.toString(indice_now));        
    }//GEN-LAST:event_btnBackActionPerformed

