    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        /*
        Para guardar los valores almaceno lo que ingrese el usuario en el correspondiente
        vector de datos, en el indice donde se encuentre actualmente.
        en el caso de los vectores intm por la limitacion de que el metodo getText solo
        devuelve String, hay que parsearlo.
        */
        vector_dni[indice_now] = Integer.parseInt(txtDni.getText());
        vector_apellido[indice_now] = txtApellido.getText();
        vector_nombre[indice_now] = txtNombre.getText();
        vector_telefono[indice_now] = Integer.parseInt(txtTelefono.getText());
        vector_domicilio[indice_now] = txtDomicilio.getText();
        vector_nacimiento[indice_now] = txtNacimiento.getText();
              
    }//GEN-LAST:event_btnSaveActionPerformed


